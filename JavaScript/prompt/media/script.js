//media aritmetica de 4 numeros 


//entrada
let primeiro_numero = parseFloat(prompt("Digite primeiro número: "))

let segundo_numero = parseFloat(prompt("Digite segundo número: "))

let terceiro_numero = parseFloat(prompt("Digite terceiro número: "))

let quarto_numero = parseFloat(prompt("Digite quarto número:"))

//processamento

let media = (primeiro_numero + segundo_numero + terceiro_numero  + quarto_numero)/4

//saída de dados

alert(`A media é = ${media}`)

